/*
Java Program to Check if a Given Integer is Odd or Even
Input : 13
Output: ODD
Input : 24
Output: EVEN
*/
import java.io.*;
import java.util.Scanner;

class GFG {

	public static void main(String[] args)
	{
		int num = 10;

		if (num % 2 == 0) {

			System.out.println("Entered Number is Even");
		}

		else {

			System.out.println("Entered Number is Odd");
		}
	}
}
